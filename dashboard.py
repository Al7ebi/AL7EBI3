    "# CELL 5 — Data Layer\n",
    "# ═══════════════════════════════════════════════════════════════\n",
    "\n",
    "def fetch(ticker, period, interval):\n",
    "    df = yf.download(ticker, period=period, interval=interval,\n",
    "                     auto_adjust=True, progress=False)\n",
    "    if df.empty: raise ValueError(f'No data: {ticker}')\n",
    "    if isinstance(df.columns, pd.MultiIndex):\n",
    "        df.columns = df.columns.droplevel(1)\n",
    "    df = df[['Open','High','Low','Close','Volume']].dropna()\n",
    "    return df\n",
    "\n",
    "\n",
    "def detect_swings(df, lookback=5, n=100):\n",
    "    sl = df.iloc[-n:].copy().reset_index()\n",
    "    swings = []\n",
    "    def ts(i):\n",
    "        for c in ['Datetime','Date']:\n",
    "            if c in sl.columns: return pd.Timestamp(sl[c].iloc[i])\n",
    "        return sl.index[i]\n",
    "    for i in range(lookback, len(sl)-lookback):\n",
    "        wh = sl['High'].iloc[i-lookback:i+lookback+1]\n",
    "        wl = sl['Low'].iloc[i-lookback:i+lookback+1]\n",
    "        if sl['High'].iloc[i] == wh.max():\n",
    "            swings.append(SwingPoint(i, ts(i), float(sl['High'].iloc[i]), 'high'))\n",
    "        if sl['Low'].iloc[i] == wl.min():\n",
    "            swings.append(SwingPoint(i, ts(i), float(sl['Low'].iloc[i]), 'low'))\n",
    "    return _dedup(swings, lookback)\n",
    "\n",
    "\n",
    "def _dedup(swings, w):\n",
    "    def keep(pts, mode):\n",
    "        if not pts: return []\n",
    "        res, grp = [], [pts[0]]\n",
    "        for p in pts[1:]:\n",
    "            if abs(p.index - grp[-1].index) <= w: grp.append(p)\n",
    "            else:\n",
    "                res.append(max(grp, key=lambda x: x.price) if mode=='high'\n",
    "                           else min(grp, key=lambda x: x.price))\n",
    "                grp = [p]\n",
    "        res.append(max(grp, key=lambda x: x.price) if mode=='high'\n",
    "                   else min(grp, key=lambda x: x.price))\n",
    "        return res\n",
    "    h = keep([s for s in swings if s.kind=='high'], 'high')\n",
    "    l = keep([s for s in swings if s.kind=='low'],  'low')\n",
    "    return sorted(h + l, key=lambda x: x.index)\n",
    "\n",
    "\n",
    "print('✅ Data layer ready')"
