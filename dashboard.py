    "# ═══════════════════════════════════════════════════════════════\n",
    "# CELL 6 — Stage 1: Draw on Liquidity (DOL)\n",
    "# ═══════════════════════════════════════════════════════════════\n",
    "\n",
    "def stage1_draw_on_liquidity(df_htf: pd.DataFrame,\n",
    "                              swings: List[SwingPoint],\n",
    "                              tolerance: float = 0.002) -> Tuple[List[LiquidityLevel], Optional[LiquidityLevel], str]:\n",
    "    \"\"\"\n",
    "    الهدف: تحديد المغناطيس السعري (DOL) قبل أي حركة.\n",
    "\n",
    "    المنطق:\n",
    "    1. رسم مستويات السيولة: قمم وقيعان HTF ذات لمسات متعددة\n",
    "    2. قياس 'الكثافة' = عدد المرات التي لامس فيها السعر المستوى\n",
    "    3. تحديد DOL = الأكثر كثافة في اتجاه التحيز الكبير\n",
    "    4. تأمين التحيز: السعر أسفل DOL → Long Bias / فوقه → Short Bias\n",
    "    \"\"\"\n",
    "    levels = []\n",
    "    current_price = float(df_htf['Close'].iloc[-1])\n",
    "\n",
    "    # ── بناء خريطة السيولة ──\n",
    "    highs = [s for s in swings if s.kind == 'high']\n",
    "    lows  = [s for s in swings if s.kind == 'low']\n",
    "\n",
    "    def count_touches(price, df, side, tol=tolerance):\n",
    "        \"\"\"كم مرة لامس السعر هذا المستوى؟ كلما زادت اللمسات = سيولة أكبر\"\"\"\n",
    "        count = 0\n",
    "        for i in range(len(df)):\n",
    "            if side == 'high':\n",
    "                if abs(float(df['High'].iloc[i]) - price) / price <= tol:\n",
    "                    count += 1\n",
    "            else:\n",
    "                if abs(float(df['Low'].iloc[i]) - price) / price <= tol:\n",
    "                    count += 1\n",
    "        return count\n",
    "\n",
    "    for h in highs:\n",
    "        touches = count_touches(h.price, df_htf, 'high')\n",
    "        if touches >= LIQUIDITY_TOUCHES:\n",
    "            levels.append(LiquidityLevel(\n",
    "                price=h.price, kind='BSL', touches=touches,\n",
    "                formed_at=h.timestamp, timeframe=HTF_INTERVAL\n",
    "            ))\n",
    "\n",
    "    for l in lows:\n",
    "        touches = count_touches(l.price, df_htf, 'low')\n",
    "        if touches >= LIQUIDITY_TOUCHES:\n",
    "            levels.append(LiquidityLevel(\n",
    "                price=l.price, kind='SSL', touches=touches,\n",
    "                formed_at=l.timestamp, timeframe=HTF_INTERVAL\n",
    "            ))\n",
    "\n",
    "    # ── إذا لم تُعثر على مستويات بلمسات كافية، خفض العتبة لضمان نتيجة ──\n",
    "    if not levels:\n",
    "        for h in highs[-5:]:\n",
    "            levels.append(LiquidityLevel(h.price, 'BSL', 1, h.timestamp, HTF_INTERVAL))\n",
    "        for l in lows[-5:]:\n",
    "            levels.append(LiquidityLevel(l.price, 'SSL', 1, l.timestamp, HTF_INTERVAL))\n",
    "\n",
    "    # ── تحديد DOL: المستوى الأكثر كثافة والأكثر قرباً في اتجاه التحيز ──\n",
    "    bsl_levels = sorted([l for l in levels if l.kind == 'BSL' and l.price > current_price],\n",
    "                         key=lambda x: (-x.touches, x.price))\n",
    "    ssl_levels = sorted([l for l in levels if l.kind == 'SSL' and l.price < current_price],\n",
    "                         key=lambda x: (-x.touches, -x.price))\n",
    "\n",
    "    # التحيز الكبير: إذا كان آخر close فوق منتصف نطاق HTF → هبوطي، وإلا صعودي\n",
    "    htf_high = float(df_htf['High'].max())\n",
    "    htf_low  = float(df_htf['Low'].min())\n",
    "    htf_mid  = (htf_high + htf_low) / 2\n",
    "    htf_bias = 'bearish' if current_price > htf_mid else 'bullish'\n",
    "\n",
    "    dol = None\n",
    "    if htf_bias == 'bearish' and ssl_levels:\n",
    "        dol = ssl_levels[0]\n",
    "    elif htf_bias == 'bullish' and bsl_levels:\n",
    "        dol = bsl_levels[0]\n",
    "    elif bsl_levels:\n",
    "        dol = bsl_levels[0]\n",
    "    elif ssl_levels:\n",
    "        dol = ssl_levels[0]\n",
    "\n",
    "    if dol:\n",
    "        dol.is_dol = True\n",
    "\n",
    "    # ── تحديد التحيز الفعلي بناءً على DOL ──\n",
    "    if dol:\n",
    "        bias = 'long'  if dol.price > current_price else 'short'\n",
    "    else:\n",
    "        bias = 'long' if htf_bias == 'bullish' else 'short'\n",
    "\n",
    "    return levels, dol, bias\n",
    "\n",
    "\n",
    "print('✅ Stage 1 (DOL) loaded')"
