    "# CELL 7 — Stage 2: SMT Divergence\n",
    "# ═══════════════════════════════════════════════════════════════\n",
    "\n",
    "def stage2_smt_divergence(df1: pd.DataFrame, df2: pd.DataFrame,\n",
    "                           bias: str,\n",
    "                           window: int = SMT_WINDOW,\n",
    "                           tol: float = SMT_TOLERANCE) -> Tuple[Optional[SMTSignal], str]:\n",
    "    \"\"\"\n",
    "    كشف الانحراف الذكي بين أصلين مترابطين.\n",
    "\n",
    "    المنطق:\n",
    "    Bearish SMT: ASSET1 صنع Higher High بينما ASSET2 صنع Lower High\n",
    "                  → Market Maker رفع الأصل الأول لاصطياد البائعين\n",
    "    Bullish SMT: ASSET1 صنع Lower Low بينما ASSET2 صنع Higher Low\n",
    "                  → Market Maker خفض الأصل الأول لاصطياد المشترين\n",
    "\n",
    "    شرط التزامن: يجب أن تكون القمتان/القاعان في نفس النافذة الزمنية\n",
    "    \"\"\"\n",
    "    # محاذاة التواريخ\n",
    "    common = df1.index.intersection(df2.index)\n",
    "    if len(common) < window * 2:\n",
    "        return None, 'insufficient_data'\n",
    "\n",
    "    a1 = df1.loc[common].iloc[-window:]\n",
    "    a2 = df2.loc[common].iloc[-window:]\n",
    "\n",
    "    # ── مقارنة القمم للتحقق من Bearish SMT ──\n",
    "    a1_high_idx = a1['High'].idxmax()\n",
    "    a2_high_idx = a2['High'].idxmax()\n",
    "    a1_h = float(a1['High'].max())\n",
    "    a2_h = float(a2['High'].max())\n",
    "\n",
    "    # قمة سابقة (النصف الأول من النافذة)\n",
    "    half = window // 2\n",
    "    a1_prev_h = float(a1.iloc[:half]['High'].max())\n",
    "    a2_prev_h = float(a2.iloc[:half]['High'].max())\n",
    "    a1_cur_h  = float(a1.iloc[half:]['High'].max())\n",
    "    a2_cur_h  = float(a2.iloc[half:]['High'].max())\n",
    "\n",
